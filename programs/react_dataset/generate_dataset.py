def try_generation(gen_function, test_function, f_args, t_args, n_tries):
    """
        Wrapper for generating question, answer, cot, and tool calls.
        Args:
            gen_function (function): function responsible for generating desired part of dataset
            test_function (function): function for testing the quality of the generated result of gen_function
            f_args (list): arguments of the gen_function
            t_args (list): arguments of the test_function excluding the result of gen_function, which is always the first argument of test_function
            n_tries (int): number of tries before EndOfGeneration Exception is raised (prevents stuck loops)
    """
    fitting=False
    tries=0
    while not fitting and tries<n_tries:
        tries+=1
        result=gen_function(*f_args)
        args=[result]+t_args
        fitting=test_function(*args)
    if tries<n_tries:
        return result
    else:
        if fitting:
            return result
        else:
            raise EndOfGeneration(f"Max. amount of tries ({n_tries}) reached during {gen_function.__name__}")

def count_possible_pipelines(dir):
    all_pipelines=0
    correct_pipelines=0
    for project_dir in os.listdir(dir):
        for compartment_dir in os.listdir(dir+"/"+project_dir):
            for file in os.scandir(dir+"/"+project_dir+"/"+compartment_dir):
                if file.is_file():
                    all_pipelines+=1
                    pipeline=json.load(open(file.path))
                    if len(pipeline["nodes"]) <= 8 and len(pipeline["nodes"]) >=2:
                        correct_pipelines+=1
    return all_pipelines, correct_pipelines

def generate_dataset_with_steps(pipelinepath, tries, model):
    """
        Generates a dataset from a pipeline. The dataset includes a question, steps of reasoning, and the fitting tool calls.
        Args:
            pipelinepath (str): path to the pipeline file. The file must contain a simplified, annotated (with node description) pipeline in json format.
        Returns:
            generated_dataset (tuple[str, str, list(str)]): contains 1) a generated question which fits the given pipeline 2) a chain-of-thought process explaining how to build the pipeline 3) the tool calls needed to retrieve the fitting node names (given in a format required by ollama)
    """
    pipeline=get_pipeline_from_file(pipelinepath)
    if len(pipeline["nodes"]) <= 8 and len(pipeline["nodes"]) >=2:
        try:
            question=try_generation(generate_question, test_question,[pipeline, model], [pipeline, model], tries)
            current_nodes=get_starting_nodes(pipeline)
            prev_steps=""
            final_reasoning=[]
            while current_nodes:
                new_nodes=[]
                for n in current_nodes:
                    node_str=json.dumps({n: pipeline["nodes"][n]},indent=3)
                    next_step=try_generation(generate_reasoning_step, test_reasoning_step,[question, node_str, pipeline, prev_steps, model], [question, node_str, pipeline, prev_steps, model],tries)
                    tc=try_generation(generate_next_tool_call, test_tool_call, [next_step, model], [next_step, model],tries)
                    tr=try_generation(generate_tool_answer, test_tool_answer, [tc, next_step, prev_steps, pipeline, model], [tc,next_step,pipeline, model],tries)
                    final_reasoning.append({"role": "assistant", "content": "<think>"+next_step.replace("<think>","").replace("</think>","")+"</think>", "tool_calls": convert_single_to_double_quotes(tc)})
                    final_reasoning.append({"role":"user", "content": "<tool_response>"+tr+"</tool_response>"})
                    prev_steps=prev_steps+"<think>"+next_step+"</think>"+"<tool_call>"+convert_single_to_double_quotes(tc)+"</tool_call>"+"<tool_response>"+tr+"</tool_response>"
                    new_nodes=new_nodes+get_next_node(pipeline, n)
                current_nodes=copy.deepcopy(new_nodes)
            answer= try_generation(generate_answer, test_answer, [question, final_reasoning, pipeline, model], [question, final_reasoning, pipeline, model], tries)
        except EndOfGeneration as e:
            log.write(str(e)+"\n")
            log.flush()
            return None
        return question, final_reasoning, answer, pipeline
    else:
        log.write("pipeline does not fulfil length criteria\n")
        log.flush()
        return None

