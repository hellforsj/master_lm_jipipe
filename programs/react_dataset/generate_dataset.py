def convert_single_to_double_quotes(text):
    """
    Converts top-level single quotes to double quotes,
    except those appearing inside already double-quoted sections.
    """
    result = []
    in_double = False
    in_single = False

    for i, ch in enumerate(text):
        if ch == '"' and not in_single:
            in_double = not in_double
            result.append(ch)
        elif ch == "'" and not in_double:
            # Toggle only for outer single quotes
            if in_single:
                in_single = False
                result.append('"')
            else:
                in_single = True
                result.append('"')
        else:
            result.append(ch)

    return ''.join(result)

def get_pipeline_from_file(filepath):
    """
        Reads a pipeline from a file.
        Args:
            filepath (str): filepath to a file containing the pipeline in json format.
        Returns:
            pipeline (str): pipeline in json format as a text
    """
    def del_comments(pipeline):
        comment_nodes=[]
        for node_id in pipeline["nodes"]:
            if pipeline["nodes"][node_id]["name"]=="jipipe:comment":
                comment_nodes.append(node_id)
        if comment_nodes!=[]:
            nodes=pipeline["nodes"]
            edges=[]
            for n_id in comment_nodes:
                del nodes[n_id]
                for e in pipeline["edges"]:
                    if e["source-node"]==n_id or e["target-node"]==n_id:
                        pass
                    else:
                        edges.append(e)
            return {"description":pipeline["description"], "nodes": nodes, "edges": edges}
        else:
            return pipeline

    pipeline=json.load(open(filepath))
    pipeline=del_comments(pipeline)
    return pipeline

def model_generate(prompt,model):
    """
        Wrapper for using model.
        Args:
            prompt (str): input prompt for the model.
            model (str): name of the model load in ollama
        Returns:
            response (str): output text of the model. Does not include tool calls etc.
    """

    response=ollama.generate(model=model, prompt=prompt)
    return response["response"].strip()

def generate_question(pipeline, model):
    """
        Generates a potential question for the training dataset. The question is based on a short pipeline.
        Args:
            pipeline (str): simplified pipeline in json format. Pipeline includes a short description of its functionality. The nodes have as attributes the name, description and changeable parameters. 
            model (str): name of the model load in ollama
        Returns:
            question (str): question generated by the model
    """
    return model_generate(QUESTION_PROMPT.format(pipeline=pipeline),model)

def get_starting_nodes(pipeline):
    """
    Returns the starting nodes of the graph.
    
    Parameters:
        pipeline (str): The pipeline in JSON format.
    
    Returns:
        list: A list of starting node IDs.
    """
    non_starting_nodes=[]
    start_nodes=[]
    for e in pipeline["edges"]:
        non_starting_nodes.append(e["target-node"])
    for n in pipeline["nodes"]:
        if n not in non_starting_nodes:
            start_nodes.append(n)
    return start_nodes

def get_next_node(pipeline, node_id):
    """
    Returns the list of nodes directly connected to the given node_id.
    
    Parameters:
        graph (dict): The graph in JSON/dict format with "nodes" and "edges".
        node_id (str): The node identifier to find neighbors for.
    
    Returns:
        list: A list of connected node IDs.
    """
    if node_id not in list(pipeline["nodes"].keys()):
        raise ValueError(f"Node '{node_id}' not found in graph.")
    
    connected = []
    for edge in pipeline["edges"]:
        if edge["source-node"] == node_id:
            connected.append(edge["target-node"])
    return connected

