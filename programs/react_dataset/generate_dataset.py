def tool_call_format(function_name, arguments):
    """
        Takes the function name and its arguments and returns the correct formatting of the tool call.
        Args:
            function_name (str): name of the tool/function
            arguments (zip(name (str), value (str))): arguments with the name (as expected by the tool) and its value
        Returns:
            tool_call (str): Text representation of the tool call.
    """
    tool_call="ToolCall(function=Function(name='"+function_name+"', arguments={"
    for name, value in arguments:
        tool_call=tool_call+"'"+name+"': '"+value+"'"
    tool_call=tool_call+"}))"
    return tool_call

