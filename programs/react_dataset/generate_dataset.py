def generate_node_search(pipeline, cot, model):
    """
        Generates tool calls for the node search for the training dataset. The tool calls are based on a short pipeline as well as a generated chain-of-thoughts explaining the pipeline.
        Args:
            pipeline (str): simplified pipeline in json format. Pipeline includes a short description of its functionality. The nodes have as attributes the name, description and changeable parameters. 
            cot (str): generated cot regarding the given pipeline.
            model (str): name of the model load in ollama
        Returns:
            tool_calls (zip(list,list)): simulated tool calls for node search. First list is the tool call, second the expected result
    """
    nodes=[]
    node_prompts=[]
    response= model_generate(NODE_SEARCH_PROMPT.format(cot=cot, pipeline=pipeline),model)
    for line in response.splitlines():
        try:
            if line[0]=="-":
                node_prompt=line.split(": ")[1]
                node_name=line.split(": ")[0][1:]
                node_prompts.append(node_prompt)
                nodes.append(node_name)
        except:
            pass
    return zip(node_prompts,nodes)

def decision(prompt,model):
    """
        Takes the response of a model and translates it into boolean.
        Args:
            prompt (str): prompt given to the model
            model (str): name of the model load in ollama
        Returns:
            answer (bool): True, if response from model is yes, otherwise False
    """
    response= model_generate(prompt,model)
    if re.search("yes", response):
        return True
    else:
        return False

