def try_generation(gen_function, test_function, f_args, t_args, n_tries):
    """
        Wrapper for generating question, answer, cot, and tool calls.
        Args:
            gen_function (function): function responsible for generating desired part of dataset
            test_function (function): function for testing the quality of the generated result of gen_function
            f_args (list): arguments of the gen_function
            t_args (list): arguments of the test_function excluding the result of gen_function, which is always the first argument of test_function
            n_tries (int): number of tries before EndOfGeneration Exception is raised (prevents stuck loops)
    """
    fitting=False
    tries=0
    while not fitting and tries<n_tries:
        tries+=1
        result=gen_function(*f_args)
        args=[result]+t_args
        fitting=test_function(*args)
    if tries<n_tries:
        return result
    else:
        if fitting:
            return result
        else:
            raise EndOfGeneration(f"Max. amount of tries ({n_tries}) reached during {gen_function.__name__}")
    
def generate_eval_questions(pipelines_dir, model, max_tries, log_file):
    """
        Generates eval questions based on a sample of the dataset questions.
        Args:
            dataset_path (str): path to the dataset path (csv format)
            question_path (str): path to where the sampled original questions are saved
            q_number (int): number of questions to generate for each question
            model (str): name of the model that generates the questions
            log_file (str): path where the log is saved
        Returns:
            eval_questions ([str]): contains generated questions based on sampled questions from dataset
    """
    eval_requests=[]
    eval_pipeline=[]
    log=open(log_file,"w")
    log.write(str(datetime.datetime.now())+"\n")
    log.flush()
    for file in os.scandir(pipelines_dir):  
            if file.is_file():
                short_pipeline=json.load(open(file.path))
                log.write(f"Generate request for {file.path}\n")
                log.flush()
                try:
                    new_request=try_generation(generate_question,test_question, [short_pipeline, model], [short_pipeline, model], max_tries)
                    eval_requests.append(new_request)
                    eval_pipeline.append(json.dumps(short_pipeline))
                except EndOfGeneration as e:
                    log.write(str(e)+"\n")
                    log.flush()
    log.write(f"Final size of eval questions: {len(eval_requests)}\n")
    log.flush()
    log.write(str(datetime.datetime.now())+"\n")
    log.flush()
    return {"request":eval_requests, "pipeline": eval_pipeline}

