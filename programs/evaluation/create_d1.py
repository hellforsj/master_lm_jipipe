def decision(prompt,model):
    """
        Takes the response of a model and translates it into boolean.
        Args:
            prompt (str): prompt given to the model
            model (str): name of the model load in ollama
        Returns:
            answer (bool): True, if response from model is yes, otherwise False
    """
    response= model_generate(prompt,model)
    if re.search("yes", response):
        return True
    else:
        return False

def test_question(question, sampled_question, model):
    """
        Tests if the question fits the criteria.
        Args: 
            question (str): generated question based on sampled question.
            sampled_question (str): question sampled from dataset
            model (str): name of the model load in ollama
        Returns:
            decision (bool): returns true, if the question is correct, false if not.
    """
    prompt=TEST_QUESTION_PROMPT_QUERIES_EVAL_DS.format(question=question, sampled_question=sampled_question)
    
    return decision(prompt,model)

def try_generation(gen_function, test_function, f_args, t_args, n_tries):
    """
        Wrapper for generating question, answer, cot, and tool calls.
        Args:
            gen_function (function): function responsible for generating desired part of dataset
            test_function (function): function for testing the quality of the generated result of gen_function
            f_args (list): arguments of the gen_function
            t_args (list): arguments of the test_function excluding the result of gen_function, which is always the first argument of test_function
            n_tries (int): number of tries before EndOfGeneration Exception is raised (prevents stuck loops)
    """
    fitting=False
    tries=0
    while not fitting and tries<n_tries:
        tries+=1
        result=gen_function(*f_args)
        args=[result]+t_args
        fitting=test_function(*args)
    if tries<n_tries:
        return result
    else:
        if fitting:
            return result
        else:
            raise EndOfGeneration(f"Max. amount of tries ({n_tries}) reached during {gen_function.__name__}")


def prepare_dataset(dataset_path, question_path):
    df=pd.read_csv(dataset_path, sep=";")
    questions=df["question"].to_list()
    sampled_questions=random.sample(questions, 50)
    question_file=open(question_path,"w")
    question_file.write(json.dumps(sampled_questions, indent=3))
    question_file.close()
    return sampled_questions
    
def generate_eval_questions(dataset_path, question_path, q_number, model, max_tries, log_file):
    """
        Generates eval questions based on a sample of the dataset questions.
        Args:
            dataset_path (str): path to the dataset path (csv format)
            question_path (str): path to where the sampled original questions are saved
            q_number (int): number of questions to generate for each question
            model (str): name of the model that generates the questions
            log_file (str): path where the log is saved
        Returns:
            eval_questions ([str]): contains generated questions based on sampled questions from dataset
    """
    sampled_questions=prepare_dataset(dataset_path, question_path)
    log=open(log_file,"w")
    log.write(f"Generating questions based on {len(sampled_questions)} questions.\nQuestions to generate: {len(sampled_questions)*q_number}\n")
    log.write(str(datetime.datetime.now())+"\n")
    log.flush()
    n=1
    query=[]
    eval_questions=[]
    for question in sampled_questions:
        for i in range(q_number):
            log.write(f"Generate question {n}\n")
            log.flush()
            n+=1
            try:
                new_question=try_generation(generate_question,test_question, [question,query,model], [question, model], max_tries)
                query.append(new_question)
                eval_questions.append(new_question)
            except EndOfGeneration as e:
                log.write(str(e)+"\n")
                log.flush()
    log.write(f"Final size of eval questions: {len(eval_questions)}\n")
    log.flush()
    log.write(str(datetime.datetime.now())+"\n")
    log.flush()
    return eval_questions

