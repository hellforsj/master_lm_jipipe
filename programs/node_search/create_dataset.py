import ollama
import re
import pandas as pd
import json
import datetime
from prompts import NODE_SEARCH_GENERATE_QUERY, NODE_SEARCH_JUDGE_PROMPT
from preprocessing import prepare_dataset

client =ollama.Client(host="http://localhost:11434")

def model_generate(prompt,model):
    """
        Wrapper for using model.
        Args:
            prompt (str): input prompt for the model.
            model (str): name of the model load in ollama
        Returns:
            response (str): output text of the model. Does not include tool calls etc.
    """

    message={
        "role":"user",
        "content":prompt
    }
    response=client.generate(model=model, prompt=prompt)
    return response.response

def decision(prompt,model):
    """
        Takes the response of a model and translates it into boolean.
        Args:
            prompt (str): prompt given to the model that is parsed for the decision
            model (str): name of the model load in ollama
        Returns:
            answer (bool): True, if response from model is yes, otherwise False
    """
    response= model_generate(prompt,model)
    if re.search("yes", response):
        return True
    else:
        return False

def generate_query(node, queries, model):
    queries="; ".join(queries)
    """
        Generates a potential query for the search of a node.
        Args:
            pipeline (str): simplified pipeline in json format. Pipeline includes a short description of its functionality. The nodes have as attributes the name, description and changeable parameters. 
            model (str): name of the model load in ollama
        Returns:
            query (str): query generated by the model
    """
    prompt=NODE_SEARCH_GENERATE_QUERY.format(node_name=node["name"],
        node_description=node["description"],
        queries=queries)
    return model_generate(prompt, model)

def test_query(query, node, model):
    """
        Tests if the query fits the task the dataset wants to solve. Makes its decision based on the given node.
        Args:
            node (str): json format of node including name and description. 
            query (str): generated query regarding the given node.
            model (str): name of the model load in ollama
        Returns:
            decision (bool): returns true, if the query is fitting, false if not.
    """
    prompt=NODE_SEARCH_JUDGE_PROMPT.format(question=query, 
        node=json.dumps(node, indent=4))
    return decision(prompt,model)

def generate_dataset(node_description_path, q_number, model, log_file):
    """
        Generates a dataset from the node descriptions. The dataset includes a question and the fitting node name.
        Args:
            node_description_path (str): path to the node description file. The file must contain the descriptions in csv format.
            q_number (int): number of questions to generate for each node
        Returns:
            generated_dataset (tuple[str, str]): contains 1) a generated question which fits the node 2) the name of the node
    """
    df=prepare_dataset(node_description_path)
    n=pd.read_csv(node_description_path).shape[0]-df.shape[0]
    intermediate_results=open("intermediate_results.csv")
    log=open(log_file,"w")
    log.write(f"Nodes not used: {n}\n")
    log.write(f"Generating prompts for {df.shape[0]} nodes.\nQuestions to generate: {q_number*df.shape[0]}\n")
    log.write(str(datetime.datetime.now())+"\n")
    log.flush()
    questions=[]
    answers=[]
    n=1
    for index, row in df.iterrows():
        queries=[]
        log.write(f"Node {index+1}/{df.shape[0]}: ")
        log.flush()
        node={"name":row["name"], "description": row["description"]}
        qs=0
        for i in range (0,q_number):
            tries=0
            fitting_question=False
            while not fitting_question and tries<20:
                question=generate_query(node, queries, model)
                fitting_question=test_query(question, node, model)
                tries+=1
            if not tries>=20:
                qs+=1
                queries.append(question)
                questions.append(question)
                answers.append(node["name"])
                intermediate_results.write(question+";"+node["name"]+"/n")
                intermediate_results.flush()
        log.write(f"{qs} Question(s)\n")
        log.flush()
    log.write(str(datetime.datetime.now())+"\n")
    log.write(f"Final size dataset: {len(questions)}")
    log.flush()
    log.close()
    intermediate_results.close()
    return pd.DataFrame({"prompt":questions, "answer": answers})

df=generate_dataset("/data/JIPipe/JIPipe_nodes/description_of_nodes.csv", 5, "gemma3:27b", "log.txt")
